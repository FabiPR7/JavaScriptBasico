//Operadores

//Numericos
let n1 = 5;
let n2 = 5;

console.log(n1 + n2);

console.log(n1 - n2);

console.log(n1 / n2);

console.log( n1 * n2 );

//Comparacion

console.log(n1 > n2);

console.log(n1 < n2);

console.log(n1 == n2);

console.log(n1 != n2);

console.log(n1 <= n2);

console.log(n1 >= n2);

//AND Y OR

console.log(n1 > n2 && n2 < n1);
console.log(n1 > n2 || n2 < n1);